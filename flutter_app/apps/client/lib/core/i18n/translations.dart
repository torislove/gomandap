/// Central translation repository for GoMandap Client Panel.
/// Supports 7 Indian languages: English, Telugu, Hindi, Tamil, Kannada, Malayalam, Bengali.
///
/// Keys use dot-notation grouping for scannability.
/// Placeholders use {{name}} syntax.
class Translations {
  const Translations._();

  static const Map<String, Map<String, String>> _all = {
    'en': _en,
    'te': _te,
    'hi': _hi,
    'ta': _ta,
    'kn': _kn,
    'ml': _ml,
    'bn': _bn,
  };

  /// Returns the translation map for a given language code.
  /// Falls back to English if the language code is unknown.
  static Map<String, String> forLanguage(String code) =>
      _all[code] ?? _en;

  /// Returns all supported language codes.
  static List<String> get supportedCodes => _all.keys.toList();
}

